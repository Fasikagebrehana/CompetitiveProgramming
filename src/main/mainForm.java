    private void employeeReportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_employeeReportMouseClicked
 
        try {
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/database1", "root", "");
    Statement stmt = conn.createStatement();
    ResultSet rs = stmt.executeQuery("SELECT * FROM employee_list");

    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Sr. No");
    model.addColumn("First Name");
    model.addColumn("Last Name");
    model.addColumn("Department");
    model.addColumn("Designation");
    model.addColumn("Number");
    model.addColumn("Address");
    model.addColumn("Salary");

    while (rs.next()) {
        String srNo = rs.getString("srno");
        String firstName = rs.getString("fname");
        String lastName = rs.getString("lname");
        String department = rs.getString("department");
        String designation = rs.getString("designation");
        String number = rs.getString("number");
        String address = rs.getString("address");
        String salary = rs.getString("salary");

        model.addRow(new Object[]{srNo, firstName, lastName, department, designation, number, address, salary});
    }

    // Create a JTable and set the model
    JTable table = new JTable(model);

    // Display the table in a JFrame or any other suitable container
    JFrame frame = new JFrame();
    frame.getContentPane().add(new JScrollPane(table));
    frame.pack();
    frame.setVisible(true);

    rs.close();
    stmt.close();
    conn.close();
} catch (SQLException ex) {
    ex.printStackTrace();
}

    }//GEN-LAST:event_employeeReportMouseClicked

