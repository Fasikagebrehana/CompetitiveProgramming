    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(this, "Are you sure to add this record?", "Add Record", JOptionPane.YES_NO_OPTION);
if (p == 0) {
    String fname = txtFname.getText().trim();
    String lname = txtLname.getText().trim();
    String department = txtDepartment.getSelectedItem().toString().trim();
    String designation = txtDesignation.getSelectedItem().toString().trim();
    String number = txtNumber.getText().trim();
    String address = txtAddress.getText().trim();
    String salary = txtSalary.getText().trim();

    if (fname.isEmpty() || lname.isEmpty() || department.isEmpty() || designation.isEmpty() || number.isEmpty() || address.isEmpty() || salary.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!fname.matches("^[a-zA-Z]+(([',. -][a-zA-Z ])?[a-zA-Z]*)*$") || !lname.matches("^[a-zA-Z]+(([',. -][a-zA-Z ])?[a-zA-Z]*)*$")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid name", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!number.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid number", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (!salary.matches("\\d+(\\.\\d+)?")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid salary", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/database1", "root", "");
            String sql = "INSERT INTO employee_list(fname, lname, department, designation, number, address, salary) VALUES (?,?,?,?,?,?,?)";
            ps = conn.prepareStatement(sql);
            ps.setString(1, fname);
            ps.setString(2, lname);
            ps.setString(3, department);
            ps.setString(4, designation);
            ps.setString(5, number);
            ps.setString(6, address);
            ps.setString(7, salary);

            ps.execute();
            JOptionPane.showMessageDialog(this, "Data saved successfully");
            reset();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
            ex.printStackTrace();
        }
    }
} else {
    JOptionPane.showMessageDialog(null, "Data not successfully saved");
}
    }//GEN-LAST:event_jButton2ActionPerformed

